				ནད་ཡམས་མུ་གེ་མེད་པའི་ལོ་ལེགས་བཀྲ་ཤིས་འཕེལ་བ། ནད་ཡམས་མུ་གེ་མེད་པའི་བཀྲ་ཤིས་སྨོན་ལམ་སྙིང་ནས་བཏབ་ཡོད།
